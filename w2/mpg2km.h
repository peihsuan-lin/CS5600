#ifndef MPG2KM
#define MPG2KM

/*
takes miles per gallon as an argument
and returns the equivalent kilometers per liter
*/
float mpg2kml(float mpg);
/* 
takes miles per gallon as an argument 
and returns the equivalent number of liters per 100 kilometers 
*/
float mpg2lphm(float mpg);
/* 
takes the liters per 100 kilometers 
and returns miles per gallon 
*/
float lph2mpg(float lph);


#endif // MPG2KM